function insert(inputNumber) //6
{
  const inputString = inputNumber.toString();
  let result = inputString[0];

  for (let i = 1; i < inputString.length; i++) 
  {
    if (inputString[i - 1] % 2 === 0 && inputString[i] % 2 === 0) 
    {
      result += '-' + inputString[i];
    } 
    else {
      result += inputString[i];
    }
  }
  return result;
}
const inputNumber = '025468';
const result = insert(inputNumber);
console.log(result, 6);

