function countArrays(arr) //53
{
  let count = 0;
  function checkArray(item)
  {
    if (Array.isArray(item))
    {
      count++;
      item.forEach(checkArray);
    }
  }
  arr.forEach(checkArray);
  return count;
}
const arr3 = [2, 8, [6], 3, 3, 5, 3, 4, [5, 4]];
const arr4 = [2, 8, [6, 3, 3], [4], 5, [3, 4, [5, 4]]];

console.log("Number of arrays inside the said array:", countArrays(arr3), 53);
console.log("Number of arrays inside the said array:", countArrays(arr4)); 