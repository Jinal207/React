function sentenceCase(str) {
  //34
  const words = str.split(" ");

  const char = words.map((word) => {
    if (word.length > 0) {
      return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
    } else {
      return "";
    }
  });
  return char.join(" ");
}
