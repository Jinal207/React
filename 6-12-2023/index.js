function humanize(M) {
  let numToString = M.toString();
  let num = parseInt(numToString[numToString.length - 1]);
  //45
  switch (num) {
    case 1:
      return M + "st";
    case 2:
      return M + "nd";
    case 3:
      return M + "rd";
    default:
      return M + "th";
  }
}
