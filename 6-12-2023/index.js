function last(arr, n = 1) //4
{
  const length = arr.length;
  if (n <= 0)
  {
    return [];
  }
  else if (n >= length)
  {
    return arr.slice();
  }
  else
  {
    return arr.slice(length - n);
  }
}
console.log(last([7, 9, 0, -2]), 4);
console.log(last([7, 9, 0, -2],3));
console.log(last([7, 9, 0, -2],6));

